#import "UIColor+MailstrapColors.h"

@implementation UIColor (MailstrapColors)
+ (UIColor *)mailstrapBlueColor {
    return [UIColor colorWithRed:8/255.0f green:111/255.0f blue:161/255.0f alpha:1.0];
}
+ (UIColor *)mailstrapDarkBlueColor {
    return [UIColor colorWithRed:5/255.0f green:79/255.0f blue:115/225.0f alpha:1.0];
}
+ (UIColor *)mailstrapGray {
    return [UIColor colorWithRed:49/255.0f green:49/255.0f blue:49/255.0f alpha:1.0];
}
@end
